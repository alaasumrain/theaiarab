-- Function to increment news view count
CREATE OR REPLACE FUNCTION increment_news_view_count(news_id UUID)
RETURNS VOID AS $$
BEGIN
  UPDATE public.ai_news
  SET view_count = COALESCE(view_count, 0) + 1,
      updated_at = NOW()
  WHERE id = news_id;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

